package com.manerfan.blog.dao.entities.article;

import javax.persistence.Entity;

import com.manerfan.common.utils.dao.common.CommonEntity;

/**
 * <pre>文章分类</pre>
 *
 * @author ManerFan 2016年2月24日
 */
@Entity(name = "CATEGORY")
public class CategoryEntity extends CommonEntity {

    /**
     * UID
     */
    private static final long serialVersionUID = -4659804831316050405L;

    /**
     * 分类名
     */
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (!super.equals(obj))
            return false;
        if (getClass() != obj.getClass())
            return false;
        CategoryEntity other = (CategoryEntity) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        return true;
    }

}
